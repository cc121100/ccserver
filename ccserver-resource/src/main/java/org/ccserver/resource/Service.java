package org.ccserver.resource;

public class Service {

	private String name;
	
	private String path;
	

	public Service() {
	}

	public Service(String name, String path) {
		this.name = name;
		this.path = path;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getPath() {
		return path;
	}

	public void setPath(String path) {
		this.path = path;
	}
	
	
}
